public class PrintConsole {
	
	
	public static void sout(String oupout) {
		System.out.println(oupout);
	}

	
	public static void soutp(String str) {
		System.out.print(str);
	}
	public static void soutp(char ch) {
		System.out.print(ch);
	}
	
	public static void soutp(double dob) {
		System.out.print(dob);
	}
	
	public static void soutp(long lon) {
		System.out.print(lon);
	}
	
	public static void soutp(int in) {
		System.out.print(in);
	}
}
